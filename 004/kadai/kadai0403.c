#include <stdio.h>

void main() {
	int i = 1, count, sum = 0;
	printf("整数を入力 : ");
	scanf("%d", &count);
	
	while(i <= count) {
		sum += i;
		i++;
	}
	
	printf("1 ~ %d までの和は %d\n", count,sum);
}