#include <stdio.h>

void main() {
	int buf, sum = 0;
	for (int i = 0; i < 4; i++) {
		printf("整数を入力 : ");
		scanf("%d", &buf);
		sum += buf;
	}
	
	printf("最後の1回が%dだと合計が100になる\n", 100 - sum);
}