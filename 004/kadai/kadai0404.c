#include <stdio.h>

void main() {
	int i = 0, buf, sum = 0;
	while(i < 4) {
		printf("整数を入力 : ");
		scanf("%d", &buf);
		sum += buf;
		i++;
	}
	
	printf("最後の1回が%dだと合計が100になる\n", 100 - sum);
}