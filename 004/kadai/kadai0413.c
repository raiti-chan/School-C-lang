#include <stdio.h>

void main() {
	int count, sum = 0;
	printf("整数を入力 : ");
	scanf("%d", &count);
	
	for (int i = 1; i <= count; i++) {
		sum += i;
	}
	
	printf("1 ~ %d までの和は %d\n", count,sum);
}