#include <stdio.h>

void main() {
	double sum = 0.0, input;
	int i = 0;
	do {
		i++;
		printf("%d 日の雨量 : ", i);
		scanf("%lf", &input);
		sum += input;
	} while(sum < 200.0);
	
	printf("\n%d 日に 200mm を超えました\n", i);
	
}