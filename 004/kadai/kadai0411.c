#include <stdio.h>

void main() {
	int result = 0, buf;
	for (int i = 1; i <= 3; i++) {
		printf("%d 回目の得点 : ", i);
		scanf("%d", &buf);
		result += buf;
	}
	
	printf("合計 : %d 点\n", result);
}