#include <stdio.h>

void main() {
	int scope;
	
	printf("範囲を入力してください > ");
	scanf("%d", &scope);
	
	for (int i = 2; i <= scope; i += 2) {
		printf("%d", i);
	}
	
	printf("\n");
}