#include <stdio.h>

void main() {
	int input;
	
	while(input) {
		puts("整数を入力(0 で終了) : ");
		scanf("%d", &input);
		printf("%d が入力されました。\n", input);
	}
	puts("繰り返しが終わりました。"); 
	
}