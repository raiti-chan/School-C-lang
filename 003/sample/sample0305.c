#include <stdio.h>

void main() {
	int val;
	
	printf("整数を入力してください : ");
	scanf("%d", &val);
	
	if (!val) {
		puts("その数は0です。");
	} else if(val > 0) {
		puts("その数は正です。");
	} else {
		puts("その数は負です。");
	}
}