#include <stdio.h>

void main() {
	int i_value = 9.99;
	double d_value = 9.99;
	
	printf("int		型変数	n の値 : %d\n", i_value);
	printf("				n / 2 : %d\n", i_value / 2);
	printf("double	型変数	x の値 : %f\n", d_value);
	printf("				x / 2 : %f\n", d_value / 2.0);
}