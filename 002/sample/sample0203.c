#include <stdio.h>

void main() {
	int value_a, value_b;

	puts("二つの整数を入力してください。");
	printf("整数 a : ");
	scanf("%d", &value_a);
	printf("整数 b : ");
	scanf("%d", &value_b);
	
	printf("a を b で割ると%dあまり%dです。\n", value_a / value_b, value_a % value_b);
	
}