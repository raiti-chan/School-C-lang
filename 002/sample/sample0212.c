#include <stdio.h>

void main() {
	int val_a, val_b;
	
	puts("二つの整数を入力してください。");
	
	printf("整数 a : ");
	scanf("%d", &val_a);
	printf("整数 b : ");
	scanf("%d", &val_b);
	
	printf("それらの平均は%fです。\n", (val_a + val_b) / 2.0);
}