#include <stdio.h>

void main() {
	int value_a, value_b;
	
	puts("二つの整数を入力してください。");
	
	printf("整数 a : ");
	scanf("%d", &value_a);
	printf("整数 b : ");
	scanf("%d", &value_b);
	
	printf("それらの平均は%dです。\n", (value_a + value_b) / 2);
}